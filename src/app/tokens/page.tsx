"use client";

import React from "react";
import ConnectWallet from "@/components/ConnectWallet";
import { userSession } from "@/lib/wallet";
import {
  getAddressBalances,
  getFungibleTokenBalances,
  AddressBalances,
  FungibleTokenBalance,
} from "@/lib/coreApi";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/Card";

export default function TokensPage() {
  const [address, setAddress] = React.useState<string>("");
  const [loading, setLoading] = React.useState(false);
  const [stx, setStx] = React.useState<AddressBalances["stx"] | null>(null);
  const [fts, setFts] = React.useState<FungibleTokenBalance[]>([]);

  const refresh = React.useCallback(async () => {
    if (!address) return;
    setLoading(true);
    const balances = await getAddressBalances(address);
    const fungibles = await getFungibleTokenBalances(address);
    setStx(balances?.stx);
    setFts(fungibles || []);
    setLoading(false);
  }, [address]);

  React.useEffect(() => {
    if (userSession.isUserSignedIn()) {
      const data = userSession.loadUserData();
      const addr =
        data?.profile?.stxAddress?.testnet ||
        data?.profile?.stxAddress?.mainnet ||
        "";
      setAddress(addr);
    } else {
      setAddress("");
    }
  }, []);

  React.useEffect(() => {
    if (address) refresh();
  }, [address, refresh]);

  return (
    <div className="min-h-screen w-full p-6 sm:p-10 space-y-8">
      <header className="flex items-center justify-between">
        <h1 className="text-2xl font-semibold text-neutral-light">Tokens</h1>
        <div className="lg:hidden">
          <ConnectWallet />
        </div>
      </header>

      {!address && (
        <Card>
          <CardContent className="pt-6">
            <p className="text-center text-gray-400">
              Connect your wallet to view balances.
            </p>
          </CardContent>
        </Card>
      )}

      {address && (
        <>
          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <Card>
              <CardHeader>
                <CardTitle>STX Balance</CardTitle>
              </CardHeader>
              <CardContent>
                <div className="text-2xl font-bold text-neutral-light">
                  {stx?.balance ? `${stx.balance} STX` : "â€”"}
                </div>
              </CardContent>
            </Card>
            <Card>
              <CardHeader>
                <CardTitle>Your Contribution</CardTitle>
              </CardHeader>
              <CardContent>
                {/* This will be implemented in a future step */}
                <div className="text-2xl font-bold text-neutral-light">
                  Coming Soon
                </div>
              </CardContent>
            </Card>
          </div>

          <Card>
            <CardHeader>
              <div className="flex items-center justify-between">
                <CardTitle>Fungible Tokens</CardTitle>
                <button
                  onClick={refresh}
                  disabled={loading}
                  className="text-sm px-3 py-1.5 rounded-md border border-gray-600 text-gray-300 hover:bg-gray-800"
                >
                  {loading ? "Refreshing..." : "Refresh"}
                </button>
              </div>
            </CardHeader>
            <CardContent>
              <div className="overflow-auto">
                <table className="w-full text-sm text-left">
                  <thead>
                    <tr className="border-b border-gray-700 text-gray-300">
                      <th className="py-2 pr-4">Asset</th>
                      <th className="py-2 pr-4">Balance</th>
                    </tr>
                  </thead>
                  <tbody className="text-gray-400">
                    {fts.map((t: FungibleTokenBalance) => (
                      <tr
                        key={t.asset_identifier}
                        className="border-b border-gray-800 hover:bg-gray-800"
                      >
                        <td className="py-2 pr-4 break-all">
                          {t.asset_identifier}
                        </td>
                        <td className="py-2 pr-4">{t.balance}</td>
                      </tr>
                    ))}
                    {fts.length === 0 && (
                      <tr>
                        <td className="py-4 text-center" colSpan={2}>
                          No fungible tokens found.
                        </td>
                      </tr>
                    )}
                  </tbody>
                </table>
              </div>
            </CardContent>
          </Card>
        </>
      )}
    </div>
  );
}
